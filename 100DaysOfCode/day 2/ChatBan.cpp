ll emotes(ll n, ll num){
    if (num<=n){
        return num*(num+1)/2;
    }else{
        ll above = num-n;
        return (n*(n+1)/2)+(above*(2*n -above -1)/2);

    }
}



void solve() {
    ll n, x; cin>>n>>x;

    ll lo=1, hi=2*n-1, ans=hi; 

    while(lo<=hi){
        ll mid = hi - (hi-lo)/2;
        if (emotes(n, mid) > x){
            //cout << mid<< " " << emotes(n,mid) << endl;
            ans = mid; 
            hi=mid-1;
        }else if (emotes(n, mid) < x){
            lo = mid+1;
        }else{
            ans=mid;
            break;
        }
    }

    cout <<  ans << endl;
    
}

